# Implements behavior for math.trunc()
# Python program to show output of floor(), ceil()

# truncate() for a positive number.
import math
print (math.floor(3.5)) # 3
print (math.trunc(3.5)) # 3
print (math.ceil(3.5))  # 4

# truncate() for a negative number.
import math
print (math.floor(-3.5)) # -4
print (math.trunc(-3.5)) # -3
print (math.ceil(-3.5))  # -3