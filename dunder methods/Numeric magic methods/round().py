# round() is a built-in Python function used to round numbers to a specified number of decimal places.
# round(number, ndigits)

print(round(15)) #15
print(round(51.6))  #52
print(round(51.5))  #52
print(round(51.4))  #51

print(round(2.665, 2))  #2.67
print(round(2.676, 2))  #2.68
print(round(2.673, 1))  #2.7